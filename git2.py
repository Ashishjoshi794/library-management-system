#branching
# branching is used for collaborating 

# to create a new branch
# git branch <branch_name> = create a new branch bit not switch to new branch 
# git checkout <branch_name> = ctreate and switch to the new branch


# to view all branches# git branch 
# you can not see the local branch in github unless you push the branch to github

# to switch between branches=> git switch {branch_name}
# to pull the branch from the github => git pull origin {branch_name}

# to delete a branch => git branch -d {branch_name}
# to internal merging between the branches => git merge {branch_name}


# pr = pull request => when you want to merge your branch to the main branch you need to create a pull request and then the owner of the repository will review your code and then merge it to the main branch
 # pr is a request to the supervisor to review and merge your code to github